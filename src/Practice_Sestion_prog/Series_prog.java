package Practice_Sestion_prog;

import java.util.Scanner;

public class Series_prog {

	public static void main(String[] args) {
		Scanner s = new Scanner (System.in);
		int sum;
		int n=s.nextInt();
		System.out.println("INPUT");
		for(int i=1;i<=n;i++) { 
			
			sum=i*i+1;
		System.out.println(sum);
	}
	}
}
